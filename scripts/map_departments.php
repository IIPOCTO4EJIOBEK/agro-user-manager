// Build NEW name -> ID mapping
$newNameToId = [];
$res = CIBlockSection::GetList([], ['IBLOCK_ID' => 3], false, ['ID', 'NAME']);
while($s = $res->Fetch()) {
    $newNameToId[$s['NAME']] = $s['ID'];
}

echo "New departments: ".count($newNameToId)."\n";

// Build OLD ID -> NEW ID mapping
$oldIdToNewId = [];
$unmapped = [];
foreach($oldDeptMap as $oldId => $oldName) {
    if(isset($newNameToId[$oldName])) {
        $oldIdToNewId[$oldId] = $newNameToId[$oldName];
    } else {
        $unmapped[$oldId] = $oldName;
    }
}

echo "Mapped: ".count($oldIdToNewId)." old IDs to new IDs\n";
if(!empty($unmapped)) {
    echo "Unmapped old departments:\n";
    foreach($unmapped as $id => $name) {
        echo "  $id => $name\n";
    }
}

// Now update all users
echo "\n=== Updating users ===\n";
$updated = 0; $errors = 0; $skipped = 0;

$res = $DB->Query("SELECT VALUE_ID, UF_DEPARTMENT FROM b_uts_user WHERE UF_DEPARTMENT IS NOT NULL AND UF_DEPARTMENT != 'a:0:{}'");
while($u = $res->Fetch()) {
    $oldDepts = @unserialize($u['UF_DEPARTMENT']);
    if(!is_array($oldDepts)) continue;
    
    $newDepts = [];
    foreach($oldDepts as $oldDeptId) {
        if(isset($oldIdToNewId[$oldDeptId])) {
            $newDepts[] = $oldIdToNewId[$oldDeptId];
        } else {
            // Try to find similar department or skip
            $skipped++;
        }
    }
    
    if(!empty($newDepts)) {
        $newDepts = array_unique($newDepts);
        $serialized = serialize($newDepts);
        $uidInt = intval($u['VALUE_ID']);
        
        $sql = "UPDATE b_uts_user SET UF_DEPARTMENT = '".$DB->ForSql($serialized)."' WHERE VALUE_ID = $uidInt";
        if($DB->Query($sql)) {
            $updated++;
        } else {
            $errors++;
        }
    }
    
    if($updated % 500 == 0 && $updated > 0) {
        echo "Progress: $updated updated, $errors errors, $skipped skipped (old IDs unmapped)\n";
    }
}

echo "\n=== COMPLETE ===\n";
echo "Updated: $updated\n";
echo "Errors: $errors\n";
echo "Skipped (unmapped old IDs): $skipped\n";

