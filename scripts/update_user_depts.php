// 4. Обновление пользователей
$res = $mysqli->query("SELECT VALUE_ID, UF_DEPARTMENT FROM b_uts_user WHERE UF_DEPARTMENT IS NOT NULL AND UF_DEPARTMENT != 'a:0:{}'");
$updated = 0;
$skipped = 0;

while($u = $res->fetch_assoc()) {
    $oldDepts = @unserialize($u['UF_DEPARTMENT']);
    if(!is_array($oldDepts)) continue;
    
    $newDepts = [];
    foreach($oldDepts as $oldDeptId) {
        if(isset($oldIdToNewId[$oldDeptId])) {
            $newDepts[] = $oldIdToNewId[$oldDeptId];
        } else {
            $skipped++;
        }
    }
    
    if(!empty($newDepts)) {
        $newDepts = array_unique($newDepts);
        $serialized = serialize($newDepts);
        $userId = $u['VALUE_ID'];
        $sql = "UPDATE b_uts_user SET UF_DEPARTMENT = '".$mysqli->real_escape_string($serialized)."' WHERE VALUE_ID = $userId";
        if($mysqli->query($sql)) {
            $updated++;
        }
    }
    
    if($updated % 200 == 0 && $updated > 0) {
        echo "   Обновлено: $updated...\n";
    }
}

